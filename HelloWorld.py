# Printing Strings with Python

## FIRST METHOD
# Directly insert between two quotes the phrase you want to print
print("Hello World")

## SECOND METHOD
# Use variables to print "Hello World"

var1 = "Hello"
var2 = "World!"

print(var1, var2)

## THIRD METHOD
# we can use f-string property for cases when the string is 
print(f"{var1} {var2}!")